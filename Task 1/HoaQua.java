public class HoaQua{

    String mauSac;
    String muiVi;
    double giaBan1Can;
    String xuatXu;

    public String getMauSac() {
        return mauSac;
    }

    public void setMauSac(String mauSac) {
        this.mauSac = mauSac;
    }

    public String getMuiVi() {
        return muiVi;
    }

    public void setMuiVi(String muiVi) {
        this.muiVi = muiVi;
    }

    public double getGiaBan1Can() {
        return giaBan1Can;
    }

    public void setGiaBan1Can(double giaBan1Can) {
        this.giaBan1Can = giaBan1Can;
    }

    public String getXuatXu() {
        return xuatXu;
    }

    public void setXuatXu(String xuatXu) {
        this.xuatXu = xuatXu;
    }
}